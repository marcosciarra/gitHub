// VARIABILI STANDARD   --> variabili globale
// visibile nell'ambiente in cui viene dichiarata o valida solo nella funzione chiamata
var test = '';

//VARIABILE VISTE SOLO ALL'INTERNO DI UN BLOCCO     --> variabili non globali
// Serve per creare variabili che devono vivere solo in un blocco (dentro un ciclo for, dentro un if .... il blocco è tutto ciò che è tra { } ) 
let test2 = '2';
{
    let test2 = 5;
    console.log(test2); //Stampa 5
}
console.log(test2);     //Stampa 2

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////    COSTANTI     ///////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Le costanti vengono assegnate e non possono essere riassegnate
// Le costanti hanno la setessa visibilità della let --> quindi visibile solo per "blocco"

const name = 'Marco';
//creo blocco
{
    const name = 'Elena';
    console.log(name);
}
console.log(name);
//Così funziona perchè le assegnazioni sono in due "blocchi" diversi

//Non posso riassegnare un array in costante ma posso aggiungere altri elementi
const frutti = ['BANANA', 'MELA', 'PESCA'];
frutti.push('MIRTILLO');


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//                                                                TYPEOF
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

let nome = 'Marco';
console.log(typeof nome);   //Restituisce il tipo della variabile

var test1;
console.log(typeof test1);  //Stampa -> Undefined

var test2 = 'Nome';
console.log(typeof test2);  //Stampa->  string

var test3 = 42;
console.log(typeof test3);  //Stampa -> number

var test4 = [1, 2, 3, 4];
console.log(typeof test4);  //Stampa -> object --> perchè in JS gli array sono Object

var test5 = { nome: 'Marco', cognome: 'Sciarra' };
console.log(typeof test5);  //Stampa object

test6 = function () {
    return 'test6';
}
console.log(typeof test6);  //Stampa -> function

var test7 = false;
console.log(typeof test6);  //Stampa -> boolean